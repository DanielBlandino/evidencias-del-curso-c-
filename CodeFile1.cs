using System;

class Program
{
    static void Main(string[] args)
    {
        // Tipos de datos
        int entero = 10;
        double decimalDoble = 20.5;
        float decimalFlotante = 5.5f;
        char caracter = 'A';
        string cadena = "Hola";
        bool esVerdadero = true;

        // Mostrar los valores
        Console.WriteLine($"Entero: {entero}");
        Console.WriteLine($"Double: {decimalDoble}");
        Console.WriteLine($"Float: {decimalFlotante}");
        Console.WriteLine($"Char: {caracter}");
        Console.WriteLine($"String: {cadena}");
        Console.WriteLine($"Bool: {esVerdadero}");
    }
}
